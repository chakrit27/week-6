package package2;

public class C {
	// Integer Literals in java
	public static void main(String[]args) {
		int a = 10; // 1.Integer value
		short s = 2; // 2.Short value
		byte b = 6; // 3.byte value
		long l = 123456; // 4.long value
		float f = 65.234f; // 5.float value
		double d = 875.765d; // 6.double value
		System.out.println("The intergral variable is " + a);
		System.out.println("The short variable is " + s);
		System.out.println("The byte variable is " + b);
		System.out.println("The long variable is " + l);
		System.out.println("The float variable is " + f);
		System.out.println("The double variable is " + d);
	}
}
